#include <iostream>
#include <cstdio>
#include <cmath>
using namespace std;

double f(double x, int n)
{
	if (n == 1)
		return sqrt(1 + x);
	return sqrt(n + f(x, n-1));
}

int main()
{
	double x;
	int n;
	while (cin >> x >> n)
	{
		printf("%.2lf\n", f(x, n));
	}
	return 0;
}
