/*
【题目描述】
在所有的N位数中，有多少个数中有偶数个数字3?由于结果可能很大，你只需要输出这个答案对12345取余的值。

【输入】
读入一个数N。

【输出】
输出有多少个数中有偶数个数字3。

【输入样例】
2
【输出样例】
73
*/
#include <iostream>
using namespace std;

int main()
{
	int n;
	while (cin >> n)
	{
		int f[100001][2] = {0}, x = 0;
		f[1][0] = 9;
		f[1][1] = 1;
		x = 9;
		for (int i = 2; i <= n; i++)
		{
			if (i == n)
				x--;
			f[i][0] = (x * f[i-1][0] + f[i-1][1]) % 12345;
			f[i][1] = (x * f[i-1][1] + f[i-1][0]) % 12345;
		}
		cout << f[n][0] << endl;
	}
	return 0;	
} 
